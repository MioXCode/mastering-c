#include <stdio.h>

/**
 * Program sederhana untuk menampilkan "Hello World"
 * Menggunakan printf untuk mencetak teks ke layar
 * @return 0 jika program berhasil dijalankan
 */
int main()
{
    // Komentar satu baris untuk menjelaskan kode
    /**
     * Komentar multi-baris yang dapat digunakan
     * untuk memberikan penjelasan yang lebih detail
     * tentang bagian kode tertentu
     */

    // Mencetak "Hello World" dengan tab (\t) sebagai pemisah
    printf("Hello\tWorld!"); // \t akan membuat jarak/tab antara Hello dan World
    return 0;                // Mengembalikan nilai 0 sebagai tanda program berhasil
};