/**
 * Program sederhana untuk menampilkan nilai konstanta PI
 * Program ini mendeklarasikan sebuah konstanta PI dengan nilai 3.18
 * dan mencetaknya ke layar menggunakan printf
 */
#include <stdio.h>

/**
 * Fungsi utama program
 * @return int Mengembalikan 0 jika program berhasil dijalankan
 */
int main()
{
    // Deklarasi konstanta PI dengan nilai 3.18
    const float PI = 3.18;
    // Mencetak nilai PI ke layar
    printf("%f", PI);

    return 0;
};